        <h3 class="negritas">Gastritis</h2>
            <div class="mensaje_1">
                <div class="Cont_Der">
                    <p><span>¿Que es?</span></p>
                    <p>La gastritis ocurre cuando el revestimiento del estómago resulta hinchado o inflamado.</p>
                    <p>La gastritis puede durar solo por un corto tiempo (gastritis águda) o perdurar durante meses o años (gastritis crónica).</p>
                    <p>Un traumatismo o una enfermedad repentina y grave, como una cirugía mayor, insuficiencia renal o el hecho de estar con un respirador pueden causar gastritis.</p>

                    <h4 class="negritas">¿Cuáles son las causas, incidencias y factores de riesgo?</h4>
                    <p><span>Las causas más comunes de gastritis son:<span></p>
                    <ol class="no-margin">
                        <li>Ciertos medicamentos como ácido acetilsalicílico (aspirina), ibuprofeno o naproxeno, cuando se toman por mucho tiempo.</li>
                        <li>Tomar demasiado alcohol.</li>
                        <li class="margin-bottom">Infección del estómago con una bacteria llamada Helicobacter pylori.</li>
                    </ol>

                    <p><span>Las causas menos comunes son:</span></p>
                    <ol class="no-margin">
                        <li>Trastornos auto-inmunitarios (como anemia perniciosa).</li>
                        <li>Reflujo de bilis hacia el estómago (reflujo biliar).</li>
                        <li>Consumo de cocaína.</li>
                        <li>Ingerir o beber sustancias corrosivas o causticas (como venenos).</li>
                        <li>Estrés extremo.</li>
                        <li class="margin-bottom">Infección viral, como citomegalovirus y el virus del herpes simple, especialmente en personas con un sistema inmunitario débil.</li>
                    </ol>
